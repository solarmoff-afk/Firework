use firework_macro::sync_adapter;

/// Реализует команды для отправки в указатель на функцию адаптера, не использует типы
/// которые хранятся в куче. Адаптер позволяет реализовать абстракцию над. При написании
/// своего адаптера вы должны реализовать match или другой способ прохода по возможным
/// командам, после чего вернуть один из вариантов в перечислении AdapterResult. Адаптер
/// пишется для взаимодействия с окном, рендерингом и ОС. Сам фреймворк и кодогенерация
/// полностью изолирована от работы с рендерингом и нативными вызовами. Адаптер работает
/// с хэндлами или же id (usize обёртка) для объектов, текстур, шрифтов и т.д. которые
/// создаются через AdapterCommand
#[sync_adapter]
#[allow(unpredictable_function_pointer_comparisons)]
#[derive(Debug, Clone, Copy, PartialEq)]
pub enum AdapterCommand<'a> {
    /// Эта команда выполняет удаление всех рендер объектов которые существуют сейчас на
    /// экране. Эта команда должна синхронно (за 1 кадр) пройтись по всем объектам которые
    /// созданы и получили AdapterHandleObject (Текстуры и шрифты не AdapterHandleObject
    /// поэтому они не должны удаляться. Эта команда только для рендер объектов которые
    /// пользователь видит на экране). Фреймворк будет вызывать эту команду в сгенерированном
    /// макросом коде при навигации чтобы очистить все объекты которые были созданы предыдущим
    /// экраном. Вызов RemoveAll должен быть безопасным, используйте систему реинкарнации ID
    /// если используйте кучу чтобы не вызывать реаллокации. Не допускайте паник при
    /// использовании команды, вместо очищения хранилища объектов рекомендуется осторожно
    /// их удалять по одному и записывать id как мёртвый чтобы его место заняли другие объекты.
    /// Это также полезно при хранилище на основе массива так как в таком случае вы будете
    /// хранить массив на стэке или в BSS
    RemoveAll,

    /// Команда для запуска цикла работы приложения. Принимает название для окна (для desktop,
    /// для других платформ это не требуется, но поле обязательно), ширину, высоту и слушатель
    /// замыкание которое принимает AdapterEvent. В реализации адаптера при получении RunLoop
    /// нужно запустить цикл и при событии пробрасывать его в listener замыкание
    RunLoop {
        title: &'static str,
        width: u32,
        height: u32,
        listener: fn(AdapterEvent),
    },

    /// Устаревшая команда для рендера, сейчас она не нужна
    Render,

    NewRect {
        layout: u16,
    },

    /// Позиции в логических координатах, здесь указывается левый верхний угол
    SetPosition(usize, (i32, i32)),

    SetSize(usize, (i32, i32)),

    /// R, G, B, A цвет от 0 до 255
    SetColor(usize, (u8, u8, u8, u8)),

    SetZ(usize, i32),

    SetVisible(usize, bool),

    Remove(usize),

    /// Принимает хит группу и x, y, w (ширина), h (высота), после чего находит примитив
    /// который попадает в такой квадрат (AABB) и возвращает хэндл примитива с самым большим
    /// Z индексом. Используется для кликов
    ResolveHit(u16, (i32, i32, i32, i32)),

    /// Устанавливает примитиву хит группу для отслеживания коллизий
    SetHitGroup(usize, u16),

    /// Создаёт новый текст, возвращая Handle
    NewText {
        layout: u16,
    },

    /// Добавление сегмента текста с указанием стиля,
    ///  - 0 обычный
    ///  - 1 жирный
    ///  - 2 курсив,
    ///  - 3 жирный курсив
    ///  - 4-256 - зарезервировано
    PushText {
        handle: usize,

        // Адаптер должен клонировать строку
        text: &'a str,
        mode: u8,
    },

    /// Очищает все части текста которые были накоплены в результате PushText
    ClearText(usize),

    MeasureText(usize),

    /// Выравнивание текста
    ///  - 0 лево
    ///  - 1 центр
    ///  - 2 право
    SetTextAlign(usize, u8),

    /// Устанавливает максимальную ширину текста в логических пикселях Если ширина больше 0 то
    /// текст переносится по словам при достижении лимита, если ширина = 0, перенос отключён
    /// (по умолчанию)
    ///
    /// Правила переноса (должны соблюдаться всеми адаптерами):
    ///  - Перенос выполняется по границам слов (пробел, табы)
    ///  - Если одно слово длиннее wrap_width, оно переносится посимвольно
    ///  - Последовательность пробелов в конце строки не переносится на следующую
    ///  - Одиночный перенос строки (\n) всегда начинает новую строку
    ///  - Перенос строки не добавляет дополнительных пробелов между словами
    ///  - MeasureText возвращает ширину самой длинной строки и суммарную высоту
    SetTextWrapWidth(usize, u32),

    /// Устанавливает скругление углов для прямоугольника по часовой стрелке для
    /// каждого угла отдельно по хэндлу прямоугольника
    SetCornerRadius(
        usize,
        (
            /* Левый верхний */ u16,
            /* Правый верхний */ u16,
            /* Правый нижний */ u16,
            /* Левый нижний */ u16,
        ),
    ),

    /// Устанавливает цветную обводку для прямоугольника. Принимает его хэндл,
    /// толщину обводки (если 0 то обводка выключена) и RGBA цвет от 0 до 255
    SetBorder(usize, u16, (u8, u8, u8, u8)),

    /// Устанавливает размер шрифта для текстового примитива в логических пикселях
    SetFontSize(usize, u16),

    /// Дае́т примитиву по хэндлу тень. SetVisible скрывает объект целиком включая
    /// его тень. цвет объекта не влияет на цвет тени
    SetShadow(
        /* Хэндл */ usize,
        /* Смещение тени x, y */ (i16, i16),
        /* Размытие */ u16,
        /* Цвет RGBA */ (u8, u8, u8, u8),
    ),

    /// Принимает два хэндла и применяет обрезку по форме
    SetClipTo(usize, usize),
}

/// Перечисления для реализации абстракции над событием EventLoop. В своей реализации
/// адаптера вы используете любой крейт для реализации работы с окном (или JS Event Loop или
/// нативным лупом системы) и отправляете в замыкание listener которое передаёт фреймворк для
/// обработки событий
#[derive(Debug, Clone, Copy)]
pub enum AdapterEvent {
    /// Клик по экрану в формате x (u32), y (u32). Спецификация старается не использовать
    /// f32 так как кроме мобильных и декстопных устройств Firework должен поддерживать
    /// embedded. Нужно вызывать. Третий аргумент это фаза клика
    Touch(u32, u32, AdapterClickPhase),

    /// > ENG: Keyboard input. The argument is the character code that was typed.
    /// > RU: Ввод с клавиатуры. Аргумент это код символа который был набран
    Key(u32),

    /// > ENG: Must be called every frame.
    /// > RU: Должен вызываться каждый кадр
    Tick,

    /// > ENG: Request to close the application
    /// > RU: Запрос на закрытие приложения
    CloseRequest,
}

/// > ENG: Enumeration for specifying the screen click phase.
/// > RU: Перечисление для указания фазы клика по экрану
#[derive(Debug, Clone, Copy)]
pub enum AdapterClickPhase {
    /// > ENG: Finger just pressed and is holding.
    /// > RU: Палец только нажал и держит
    Began,

    /// > ENG: Finger pressed (Began) and is moving across the screen.
    /// > RU: Палец нажал (Began) и водит по экрану
    Moved,

    /// > ENG: Finger was released.
    /// > RU: Палец отпустил
    Ended,

    /// > ENG: Click was cancelled.
    /// > RU: Отмена клика
    Cancelled,
}

#[derive(Debug, Clone, Copy)]
pub enum AdapterResult {
    Void,
    Fail,
    Handle(usize),
    Size(/* Ширина */ u32, u32),
}
